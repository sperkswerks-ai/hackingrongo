def _format_size(path: Path) -> str:
    if not path.exists():
        return ""
    kb = path.stat().st_size / 1024
    if kb >= 1024:
        return f"{kb/1024:.1f} MB"
    return f"{kb:.0f} KB"


def _render_card(
    rel_path: str,
    title: str,
    desc: str,
    bundle_root: Path,
) -> str:
    physical = bundle_root / rel_path
    exists = physical.exists()
    size_str = _format_size(physical) if exists else ""

    card_cls = "report-card" if exists else "report-card missing"
    link_html = (
        f'<a class="open-btn" href="{_html.escape(rel_path)}" target="_blank">Open ↗</a>'
        if exists else
        '<span class="missing-tag">not generated this run</span>'
    )
    return (
        f'<div class="{card_cls}">'
        f'<div class="report-name">{_html.escape(title)}</div>'
        f'<div class="report-desc">{_html.escape(desc)}</div>'
        f'<div class="report-footer">'
        f'<span class="report-size">{_html.escape(size_str)}</span>'
        f'{link_html}'
        f'</div>'
        f'</div>'
    )


def build_index(
    bundle_root: Path,
    catalogued: list[tuple[str, str, str, str]],
    extras: list[Path],
    generated: str,
    n_total: int,
) -> str:
    # Count what actually exists
    n_present = sum(1 for path, *_ in catalogued if (bundle_root / path).exists())
    n_present += len(extras)

    # TOC
    toc_links = "\n".join(
        f'<a href="#{sid}">{_html.escape(_SECTIONS[sid][0])}</a>'
        for sid in _SECTION_ORDER
        if any(s == sid for _, _, s, _ in catalogued) or (sid == "other" and extras)
    )

    # Group catalogue by section
    by_section: dict[str, list[tuple[str, str, str]]] = {s: [] for s in _SECTION_ORDER}
    for path, title, section, desc in catalogued:
        by_section.setdefault(section, []).append((path, title, desc))

    # Extras in "other"
    for p in sorted(extras):
        rel = str(p.relative_to(bundle_root)).replace("\\", "/")
        by_section["other"].append((rel, p.stem.replace("_", " ").title(), ""))

    sections_html = ""
    for sid in _SECTION_ORDER:
        items = by_section.get(sid, [])
        if not items:
            continue
        sec_title, sec_desc = _SECTIONS[sid]
        accent = _SECTION_ACCENT.get(sid, "var(--accent)")
        cards = "\n".join(_render_card(path, title, desc, bundle_root) for path, title, desc in items)
        sections_html += (
            f'<div class="section" id="{sid}">'
            f'<div class="section-title" style="border-color:{accent}">'
            f'{_html.escape(sec_title)}</div>'
            f'<div class="section-desc">{_html.escape(sec_desc)}</div>'
            f'<div class="report-grid">{cards}</div>'
            f'</div>'
        )

    return f"""\
<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="utf-8">
<meta name="viewport" content="width=device-width, initial-scale=1">
<title>Rongorongo — Pipeline Report Index</title>
<style>{_CSS}</style>
</head>
<body>
<nav class="toc">
  <div class="toc-title">Sections</div>
  {toc_links}
</nav>
<div class="wrap">
  <div class="header">
    <div class="title">Rongorongo Decipherment Pipeline</div>
    <div class="subtitle">Report bundle — open any report to review findings</div>
    <div class="meta-row">
      <span><b>Generated</b> {_html.escape(generated)}</span>
      <span><b>Reports present</b> {n_present} / {n_total}</span>
      <span><b>Bundle</b> reports_bundle.zip</span>
    </div>
  </div>
  {sections_html}
</div>
</body>
</html>
"""


