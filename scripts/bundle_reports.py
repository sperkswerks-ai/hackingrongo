def bundle(outputs_dir: Path) -> Path:
    """Build the bundle. Returns path to the .zip file."""
    bundle_root = outputs_dir / "reports_bundle"
    if bundle_root.exists():
        shutil.rmtree(bundle_root)
    bundle_root.mkdir(parents=True)

    # Track which relative paths we've copied (to detect extras)
    catalogued_relpaths = {entry[0] for entry in _CATALOGUE}
    copied_relpaths: set[str] = set()

    # Copy catalogued reports (preserving sub-paths)
    for rel_path, *_ in _CATALOGUE:
        src = outputs_dir / rel_path
        dest = bundle_root / rel_path
        dest.parent.mkdir(parents=True, exist_ok=True)
        if src.exists():
            shutil.copy2(src, dest)
            copied_relpaths.add(rel_path)

    # Copy all other HTMLs found in outputs/ that aren't in the catalogue
    extras: list[Path] = []
    for src in sorted(outputs_dir.rglob("*.html")):
        rel = str(src.relative_to(outputs_dir)).replace("\\", "/")
        if rel in catalogued_relpaths or "reports_bundle" in rel:
            continue
        dest = bundle_root / rel
        dest.parent.mkdir(parents=True, exist_ok=True)
        shutil.copy2(src, dest)
        extras.append(bundle_root / rel)

    # Generate index
    generated = datetime.now(tz=timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")
    n_total = len(_CATALOGUE) + len(extras)
    index_html = build_index(bundle_root, _CATALOGUE, extras, generated, n_total)
    (bundle_root / "index.html").write_text(index_html, encoding="utf-8")

    # Zip the bundle
    zip_path = outputs_dir / "reports_bundle.zip"
    with zipfile.ZipFile(zip_path, "w", zipfile.ZIP_DEFLATED) as zf:
        for f in sorted(bundle_root.rglob("*")):
            if f.is_file():
                zf.write(f, f.relative_to(bundle_root))
    return zip_path


def main() -> None:
    p = argparse.ArgumentParser(description="Bundle all pipeline HTML reports for download.")
    p.add_argument(
        "--outputs-dir", type=Path,
        default=PROJECT_ROOT / "outputs",
        help="Root outputs directory (default: outputs/)",
    )
    args = p.parse_args()

    if not args.outputs_dir.exists():
        print(f"outputs dir not found: {args.outputs_dir}")
        sys.exit(1)

    print("Bundling reports…")
    zip_path = bundle(args.outputs_dir)

    bundle_root = args.outputs_dir / "reports_bundle"
    all_html = sorted(bundle_root.rglob("*.html"))
    total_kb = zip_path.stat().st_size / 1024

    print(f"\n{'━'*60}")
    print(f"  Report bundle complete")
    print(f"  {len(all_html)} HTML files  ({total_kb:.0f} KB zipped)")
    print(f"  Index:  {bundle_root}/index.html")
    print(f"  Zip:    {zip_path}")
    print(f"{'━'*60}")
    print("\n  Open index.html or download reports_bundle.zip")


if __name__ == "__main__":
    main()
