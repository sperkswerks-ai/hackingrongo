"""
scripts/bundle_reports.py

Collects every HTML report generated by the hackingrongo pipeline, creates
a navigable index.html, and zips the whole bundle for one-click download.

What it does
------------
1.  Scans the entire outputs/ tree for .html files.
2.  Copies them into outputs/reports_bundle/ preserving sub-paths so
    relative links (e.g. passage_reports/index.html → P007_ADHS.html) work.
3.  Writes outputs/reports_bundle/index.html — a dark-terminal landing page
    that organises every report into labelled sections with descriptions.
4.  Creates outputs/reports_bundle.zip for one-click download.

Run at the end of any pipeline session:

    python scripts/bundle_reports.py
    python scripts/bundle_reports.py --outputs-dir /path/to/outputs
"""

