"""
hackingrongo.results.report
======================

Generates a human-readable scholarly report from a
:class:`~hackingrongo.results.schema.HypothesisRanking`.

The report is structured as a multi-section Markdown document intended
to accompany a paper submission.  Unlike Souza (2022), whose ``results.csv``
and ``stats.txt`` require code to interpret, this output is directly
readable by a human reviewer.

Sections
--------
1. **Preamble** — experiment metadata, config hash, generation timestamp.
2. **Hypothesis ranking** — table of top N hypotheses with quality metrics.
3. **Top hypothesis: sign assignments** — per-sign phoneme mapping with
   confidence and evidence counts.
4. **Stratum consistency analysis** — cross-stratum LM scores for the
   top hypothesis.
5. **Comparison to prior proposals** — how assignments differ from
   Fischer (1997) and Horley (2021) where known readings exist in the
   catalog.

Public API
----------
``generate_report``
    Main entry point.  Returns a Markdown string.
"""

from __future__ import annotations

import logging
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

from omegaconf import DictConfig

from hackingrongo.results.schema import (
    DecryptionHypothesis,
    HypothesisRanking,
    PhonemeAssignment,
    StratumScore,
)

logger = logging.getLogger(__name__)

# Known phoneme proposals from prior literature, keyed by Barthel code.
# These are partial; the full comparison requires the sign_metadata.json.
# Source: Fischer (1997) "RongoRongo, the Easter Island Script", p.382-411
# and Horley (2021) tentative readings where explicitly stated.
_PRIOR_PROPOSALS: dict[str, dict[str, str]] = {
    # Format: {"fischer_1997": "...", "horley_2021": "..."}
    # Populated from sign_metadata.json at report time when available.
}


# ---------------------------------------------------------------------------
# Main entry point
# ---------------------------------------------------------------------------


def generate_report(
    ranking: HypothesisRanking,
    cfg: DictConfig,
    top_n: int = 5,
    prior_proposals: dict[str, dict[str, str]] | None = None,
) -> str:
    """Generate a Markdown scholarly report from a hypothesis ranking.

    Parameters
    ----------
    ranking : HypothesisRanking
        Ranked decipherment hypotheses from a completed pipeline run.
    cfg : DictConfig
        Root Hydra config.  Used for experiment metadata in the preamble.
    top_n : int
        Number of top hypotheses to detail.  Defaults to ``5``.
    prior_proposals : dict[str, dict[str, str]], optional
        Maps Barthel code → ``{"fischer_1997": "...", "horley_2021":
        "..."}`` for the comparison section.  If ``None``, this section
        is omitted.

    Returns
    -------
    str
        Complete Markdown document.
    """
    top_hypotheses = ranking.top_n(top_n)

    sections: list[str] = [
        _section_preamble(ranking, cfg),
        _section_ranking_table(ranking, top_n),
    ]

    if top_hypotheses:
        best = top_hypotheses[0]
        sections.append(_section_sign_assignments(best))
        sections.append(_section_stratum_analysis(best))
        if prior_proposals:
            sections.append(
                _section_prior_proposals_comparison(best, prior_proposals)
            )

    if len(top_hypotheses) > 1:
        sections.append(_section_additional_hypotheses(top_hypotheses[1:]))

    report = "\n\n---\n\n".join(sections)
    logger.info(
        "Generated decipherment report: %d hypotheses, top_n=%d.",
        len(ranking.hypotheses),
        top_n,
    )
    return report


# ---------------------------------------------------------------------------
# Section generators
# ---------------------------------------------------------------------------


def _section_preamble(ranking: HypothesisRanking, cfg: DictConfig) -> str:
    """Build the report preamble with experiment metadata."""
    mlflow_exp = str(cfg.mlflow.experiment_name)
    dataset_ver = str(cfg.mlflow.run_tags.get("dataset_version", "unknown"))
    generated = datetime.now(tz=timezone.utc).isoformat()
    n_hypotheses = len(ranking.hypotheses)

    return (
        "# hackingrongo Decipherment Report\n\n"
        "> **Generated by:** hackingrongo hybrid decipherment pipeline  \n"
        f"> **Experiment:** `{mlflow_exp}`  \n"
        f"> **Dataset version:** {dataset_ver}  \n"
        f"> **Generated at:** {generated}  \n"
        f"> **Ranking generated at:** {ranking.generated_at}  \n"
        f"> **Ranking metric:** `{ranking.ranking_metric}`  \n"
        f"> **Total hypotheses evaluated:** {n_hypotheses}  \n\n"
        "This report presents the top decipherment hypotheses produced by the "
        "hackingrongo pipeline. Each hypothesis assigns phoneme values to rongorongo "
        "glyphs and is evaluated against Polynesian language models across three "
        "temporal strata. Confidence intervals are derived from MCMC posterior "
        "samples and bootstrap validation."
    )


def _section_ranking_table(
    ranking: HypothesisRanking, top_n: int
) -> str:
    """Build the ranking summary table."""
    top = ranking.top_n(top_n)
    lines: list[str] = [
        f"## Hypothesis Ranking (Top {top_n})",
        "",
        "| Rank | ID | Type | LM Score | MCMC Log-Post | Beam Score | Assignments |",
        "|------|----|------|----------|---------------|------------|-------------|",
    ]
    for rank, hyp in enumerate(top, start=1):
        lines.append(
            f"| {rank} | `{hyp.hypothesis_id}` | {hyp.hypothesis_type} | "
            f"{hyp.overall_lm_score:.4f} | {hyp.mcmc_log_posterior:.4f} | "
            f"{hyp.beam_score:.4f} | {len(hyp.assignments)} |"
        )
    return "\n".join(lines)


def _section_sign_assignments(hyp: DecryptionHypothesis) -> str:
    """Build the per-sign phoneme assignment table for the top hypothesis."""
    lines: list[str] = [
        f"## Top Hypothesis: Sign Assignments (`{hyp.hypothesis_id}`)",
        "",
        f"**Type:** {hyp.hypothesis_type}  ",
        f"**Overall LM score:** {hyp.overall_lm_score:.4f}  ",
        f"**MCMC log-posterior:** {hyp.mcmc_log_posterior:.4f}  ",
        f"**Run ID:** `{hyp.run_id}`  ",
        f"**Config hash:** `{hyp.config_hash[:16]}…`",
        "",
        "The table below lists all phoneme assignments in this hypothesis. "
        "Confidence is the normalised posterior probability. "
        "Evidence count is the number of distinct corpus positions contributing "
        "to this assignment's score.",
        "",
        "| Sign (Barthel) | Assigned Phoneme | Confidence | Evidence |",
        "|----------------|-----------------|------------|----------|",
    ]
    for a in sorted(hyp.assignments, key=lambda x: (-x.confidence, x.sign_code)):
        conf_bar = _confidence_bar(a.confidence)
        lines.append(
            f"| `{a.sign_code}` | **{a.phoneme}** | "
            f"{a.confidence:.4f} {conf_bar} | {a.evidence_count} |"
        )
    return "\n".join(lines)


def _section_stratum_analysis(hyp: DecryptionHypothesis) -> str:
    """Build the cross-stratum consistency section."""
    lines: list[str] = [
        "## Stratum Consistency Analysis",
        "",
        "Cross-stratum consistency measures how well the hypothesis's phoneme "
        "assignments score against Polynesian language models within each "
        "temporal stratum. A hypothesis passes validation when at least "
        "`cross_stratum_min_languages` language models score above the random "
        "baseline in each stratum.",
        "",
        "| Stratum | Consistency | LM Mean (log₂) | LM Std | Passages | Languages |",
        "|---------|-------------|----------------|--------|----------|-----------|",
    ]
    for s in hyp.stratum_scores:
        langs = ", ".join(f"`{l}`" for l in s.languages_above_baseline) or "—"
        indicator = "✓" if s.languages_above_baseline else "✗"
        lines.append(
            f"| {s.stratum} {indicator} | {s.consistency_score:.4f} | "
            f"{s.lm_score_mean:.4f} | {s.lm_score_std:.4f} | "
            f"{s.n_passages} | {langs} |"
        )
    return "\n".join(lines)


def _section_prior_proposals_comparison(
    hyp: DecryptionHypothesis,
    prior_proposals: dict[str, dict[str, str]],
) -> str:
    """Build the comparison-to-prior-proposals section."""
    lines: list[str] = [
        "## Comparison to Prior Proposals",
        "",
        "The table below compares this hypothesis's assignments against "
        "published proposals from Fischer (1997) and Horley (2021) for signs "
        "where those authors provide explicit readings.",
        "",
        "| Sign | This Hypothesis | Fischer (1997) | Horley (2021) | Match |",
        "|------|-----------------|----------------|---------------|-------|",
    ]

    # Build assignment lookup
    hyp_assignments: dict[str, str] = {
        a.sign_code: a.phoneme for a in hyp.assignments
    }

    compared = 0
    for code in sorted(prior_proposals):
        priors = prior_proposals[code]
        our = hyp_assignments.get(code, "—")
        fischer = priors.get("fischer_1997", "—")
        horley = priors.get("horley_2021", "—")
        matches = [s for s in [fischer, horley] if s != "—" and s == our]
        match_str = f"{len(matches)}/{sum(1 for s in [fischer, horley] if s != '—')}"
        lines.append(
            f"| `{code}` | **{our}** | {fischer} | {horley} | {match_str} |"
        )
        compared += 1

    if compared == 0:
        lines.append("| *(No prior proposals available for comparison)* | | | | |")

    return "\n".join(lines)


def _section_additional_hypotheses(
    hypotheses: list[DecryptionHypothesis],
) -> str:
    """Compact summary of hypotheses ranked 2nd and below."""
    lines: list[str] = [
        "## Additional Hypotheses",
        "",
        "Brief summary of lower-ranked hypotheses for completeness.",
        "",
        "| Rank | ID | Type | LM Score | Top Assignment |",
        "|------|----|------|----------|----------------|",
    ]
    for rank, hyp in enumerate(hypotheses, start=2):
        top_assign = (
            max(hyp.assignments, key=lambda a: a.confidence)
            if hyp.assignments
            else None
        )
        top_str = (
            f"`{top_assign.sign_code}`→{top_assign.phoneme}"
            if top_assign
            else "—"
        )
        lines.append(
            f"| {rank} | `{hyp.hypothesis_id}` | {hyp.hypothesis_type} | "
            f"{hyp.overall_lm_score:.4f} | {top_str} |"
        )
    return "\n".join(lines)


