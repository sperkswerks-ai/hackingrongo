def _confidence_bar(confidence: float, width: int = 5) -> str:
    """Build a Unicode block-character confidence bar for Markdown tables.

    Parameters
    ----------
    confidence : float
        Value in ``[0, 1]``.
    width : int
        Total bar width in characters.

    Returns
    -------
    str
        E.g. ``"█████"`` for confidence ≥ 1.0 or ``"██░░░"`` for ~0.4.
    """
    filled = round(confidence * width)
    filled = max(0, min(width, filled))
    return "█" * filled + "░" * (width - filled)


def save_report(
    ranking: HypothesisRanking,
    cfg: DictConfig,
    output_path: Path,
    top_n: int = 5,
    prior_proposals: dict[str, dict[str, str]] | None = None,
) -> None:
    """Generate and write the report to a Markdown file.

    Parameters
    ----------
    ranking : HypothesisRanking
        Ranked decipherment hypotheses.
    cfg : DictConfig
        Root Hydra config.
    output_path : Path
        Destination ``.md`` file path.  Parent directories are created
        if needed.
    top_n : int
        Number of top hypotheses to detail.
    prior_proposals : dict[str, dict[str, str]], optional
        Prior phoneme proposals for the comparison section.
    """
    report_text = generate_report(ranking, cfg, top_n=top_n, prior_proposals=prior_proposals)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(report_text, encoding="utf-8")
    logger.info("Decipherment report written to %s.", output_path)
